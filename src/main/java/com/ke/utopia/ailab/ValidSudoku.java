package com.ke.utopia.ailab;

/**
 * 类名：ValidSudoku
 * 描述：请你判断一个 9 x 9 的数独是否有效。只需要 根据以下规则 ，验证已经填入的数字是否有效即可。
 * <p>
 * 数字 1-9 在每一行只能出现一次。
 * 数字 1-9 在每一列只能出现一次。
 * 数字 1-9 在每一个以粗实线分隔的 3x3 宫内只能出现一次。（请参考示例图）
 * <p>
 * 注意：
 * <p>
 * 一个有效的数独（部分已被填充）不一定是可解的。
 * 只需要根据以上规则，验证已经填入的数字是否有效即可。
 * 空白格用 '.' 表示。
 * <p>
 * 示例 1：
 * <p>
 * 输入：board =
 * [["5","3",".",".","7",".",".",".","."]
 * ,["6",".",".","1","9","5",".",".","."]
 * ,[".","9","8",".",".",".",".","6","."]
 * ,["8",".",".",".","6",".",".",".","3"]
 * ,["4",".",".","8",".","3",".",".","1"]
 * ,["7",".",".",".","2",".",".",".","6"]
 * ,[".","6",".",".",".",".","2","8","."]
 * ,[".",".",".","4","1","9",".",".","5"]
 * ,[".",".",".",".","8",".",".","7","9"]]
 * 输出：true
 * 示例 2：
 * <p>
 * 输入：board =
 * [["8","3",".",".","7",".",".",".","."]
 * ,["6",".",".","1","9","5",".",".","."]
 * ,[".","9","8",".",".",".",".","6","."]
 * ,["8",".",".",".","6",".",".",".","3"]
 * ,["4",".",".","8",".","3",".",".","1"]
 * ,["7",".",".",".","2",".",".",".","6"]
 * ,[".","6",".",".",".",".","2","8","."]
 * ,[".",".",".","4","1","9",".",".","5"]
 * ,[".",".",".",".","8",".",".","7","9"]]
 * 输出：false
 * 解释：除了第一行的第一个数字从 5 改为 8 以外，空格内其他数字均与 示例1 相同。 但由于位于左上角的 3x3 宫内有两个 8 存在, 因此这个数独是无效的。
 * <p>
 * <p>
 * 提示：
 * <p>
 * board.length == 9
 * board[i].length == 9
 * board[i][j] 是一位数字（1-9）或者 '.'
 * 作者：zifengchen
 * 日期：2025/12/3
 */
public class ValidSudoku {

    public static void main(String[] args) {
        char[][] board1 = {
                {'5','3','.','.','7','.','.','.','.'},
                {'6','.','.','1','9','5','.','.','.'},
                {'.','9','8','.','.','.','.','6','.'},
                {'8','.','.','.','6','.','.','.','3'},
                {'4','.','.','8','.','3','.','.','1'},
                {'7','.','.','.','2','.','.','.','6'},
                {'.','6','.','.','.','.','2','8','.'},
                {'.','.','.','4','1','9','.','.','5'},
                {'.','.','.','.','8','.','.','7','9'}
        };

        // 测试用例2：无效数独（3x3子矩阵有重复）
        char[][] board2 = {
                {'8','3','.','.','7','.','.','.','.'},
                {'6','.','.','1','9','5','.','.','.'},
                {'.','9','8','.','.','.','.','6','.'},
                {'8','.','.','.','6','.','.','.','3'},
                {'4','.','.','8','.','3','.','.','1'},
                {'7','.','.','.','2','.','.','.','6'},
                {'.','6','.','.','.','.','2','8','.'},
                {'.','.','.','4','1','9','.','.','5'},
                {'.','.','.','.','8','.','.','7','9'}
        };

        System.out.println("测试用例1结果: " + isValidSudoku(board1)); // 应该输出true
        System.out.println("测试用例2结果: " + isValidSudoku(board2)); // 应该输出false
    }


    public static boolean isValidSudoku(char[][] board) {

        // 判断是不是数独就看是不是不符合条件了
        for (int i = 0; i < 9; i++) {
            boolean[] rowChecked = new boolean[9];
            boolean[] colChecked = new boolean[9];
            for (int j = 0; j < 9; j++) {
                int num = board[i][j] - '1';
                if (board[i][j] == '.') {
                    continue;
                }
                if (rowChecked[num]) {
                    return false;
                }

                rowChecked[num] = true;
            }
            for (int j = 0; j < 9; j++) {
                int num = board[j][i] - '1';
                if (board[j][i] == '.') {
                    continue;
                }
                if (colChecked[num]) {
                    return false;
                }
                colChecked[num] = true;
            }
        }
        // 检查3*3的子矩阵
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                boolean[] checked = new boolean[9];
                for (int rIndex = i * 3; rIndex < i * 3 + 3; rIndex++) {
                    for (int cIndex = j * 3; cIndex < j * 3 + 3; cIndex++) {
                        char c = board[rIndex][cIndex];
                        int num = c - '1';
                        if (c == '.') {
                            continue;
                        }
                        if (checked[num]) {
                            return false;
                        }
                        checked[num]=true;
                    }
                }
            }
        }

        return true;
    }

}
